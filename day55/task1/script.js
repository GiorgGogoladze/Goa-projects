let num1 = parseFloat(prompt("პირველი რიცხვი:"));
let num2 = parseFloat(prompt("მეორე რიცხვი:"));

// მათემატიკური ოპერაციები
let sumResult = num1 + num2;
let subResult = num1 - num2;
let mulResult = num1 * num2;
let divResult = num1 / num2;

// შედეგების დაბეჭდვა
console.log(`${num1} + ${num2} = ${sumResult}`);
console.log(`${num1} - ${num2} = ${subResult}`);
console.log(`${num1} * ${num2} = ${mulResult}`);
console.log(`${num1} / ${num2} = ${divResult}`);
