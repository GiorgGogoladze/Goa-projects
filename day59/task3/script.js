function compareNumbers(num1, num2) {
    console.log(`Comparing ${num1} and ${num2}:`);
    console.log(`${num1} > ${num2}:`, num1 > num2);
    console.log(`${num1} >= ${num2}:`, num1 >= num2);
    console.log(`${num1} < ${num2}:`, num1 < num2);
    console.log(`${num1} <= ${num2}:`, num1 <= num2);
    console.log(`${num1} == ${num2}:`, num1 == num2);
    console.log(`${num1} != ${num2}:`, num1 != num2);
}

compareNumbers(5, 10);
